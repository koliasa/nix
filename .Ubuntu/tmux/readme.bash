# Закриття панелей
# Виконайте команду exit або натисніть Ctrl-d в активній панелі.
# Закрити панель без виходу з програми:
# Переконайтеся, що ви знаходитесь у панелі, яку хочете закрити, і виконайте Ctrl-a та x.
# Це викликає підтвердження закриття панелі (kill-pane).
# Щоб вийти з сесії, використовуйте команду:
Ctrl+b d
# Це відключить вас від поточної сесії, але вона залишатиметься активною у фоновому режимі (панелі і вікна не закриваються).
# Щоб повернутися до вже існуючої сесії, використовуйте команду:
tmux attach-session
# Якщо у вас кілька сесій, вам потрібно вказати назву сесії, до якої ви хочете підключитись:
tmux attach-session -t <назва-сесії>
# Щоб переглянути список усіх сесій, використовуйте:
tmux list-sessions

